#KEY CONCEPTS: math operators +, -, *, /, //, %, **

add = 7 + 2
print("Sum: ", add)

subtract = 7 - 2
print("Difference: ", subtract)

multiply = 7 * 2
print("Product: ", multiply)

divide = 7 / 2
print("Quotient: ", divide)

divide = 6 / 2
print("Quotient: ", divide)

float_divide = 6 / 2
print("Float Division: ", float_divide)

#One forward slash will always be a float

integer_divide = 7 // 2
print("Integer division: ", integer_divide)

#Two forward slashes will round down from a float

modulus = 7 % 2
print("Modulus: ", modulus)

exponent = 7 ** 2
print("Exponent: ", exponent)

print(2 + 2 == 10 - 6)

print(2 + 2 != 10 - 6)

